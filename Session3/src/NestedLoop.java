
public class NestedLoop {

	public static void main(String[] args) {
		
		for(int i=0; i<5; i++) {
			
			System.out.println(">> i is: "+i);
			
			System.out.println(">> j is: ");
			
			for(int j=0;j<3;j++) {
				System.out.print(j+" ");
			}
			
			System.out.println();
			
		}
		
		
		// Infinite For Loop
		//for(;;) {
			
		//}

	}

}
